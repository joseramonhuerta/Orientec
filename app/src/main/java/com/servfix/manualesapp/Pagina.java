package com.servfix.manualesapp;

public class Pagina {
    private int id_pagina;
    private String url_pagina;
    private String titulo_pagina;
    private String numero_pagina;

    public int getId_pagina() {
        return id_pagina;
    }

    public void setId_pagina(int id_pagina) {
        this.id_pagina = id_pagina;
    }

    public String getUrl_pagina() {
        return url_pagina;
    }

    public void setUrl_pagina(String url_pagina) {
        this.url_pagina = url_pagina;
    }

    public String getTitulo_pagina() {
        return titulo_pagina;
    }

    public void setTitulo_pagina(String titulo_pagina) {
        this.titulo_pagina = titulo_pagina;
    }

    public String getNumero_pagina() {
        return numero_pagina;
    }

    public void setNumero_pagina(String numero_pagina) {
        this.numero_pagina = numero_pagina;
    }
}
